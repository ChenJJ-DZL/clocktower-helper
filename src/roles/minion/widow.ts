- 除了观看魔典之外，你还能对一名玩家投毒！如果你不确定该对谁投毒，就对能获得最多潜在信息的镇民角色投毒。这能保证善良阵营在整局游戏中都获得非常糟糕的信息。可以选择对博学者、侍女、渔夫或气球驾驶员投毒实在是不容错过的体验。尽力去做吧。（如果你不确定谁是你觉得最厉害的，那就选你觉得最能伤到你的人！）
- 如果有一个简单而明显的目标投毒角色可选，那就转而对一个其他角色投毒。因为善良阵营会知道寡妇在场，他们会寻找寡妇最明显可能会选择的选项。如果你没有选择最明显的可选项，那么善良阵营会假定一名拥有强大信息能力的镇民中毒而他其实没有，同时假定一个可能性较低的角色健康而他其实也不是。多么狡诈呀。
- 此外，你可以尝试对一名你认为会指挥小镇讨论的玩家投毒，希望利用他们（现在虚假）的信息来达成你的目的。
- 不要对一名你能转而让恶魔杀死的玩家投毒。比如，当你对厨师投毒时，最好让恶魔杀死占卜师。既然你看过魔典，那么在游戏早期就和恶魔沟通杀死哪些玩家就会变得很容易。
- 和你的恶魔协作来让他们不会杀死你投毒的对象——除非那是类似守鸦人这样的角色。否则你的中毒可能会在游戏早期就被一名能关注到相同目标的睿智恶魔浪费掉！
- 尽你所能让知道你在场的玩家受到怀疑：如果小镇认为他们在撒谎，他们可能会认为他们在你在场这件事上也在撒谎！你可以通过许多社交上的方法来达成这一点，但你也可以尝试用你造成的中毒（对那名玩家本身或是对能够证明那名玩家的镇民）来瓦解那名玩家受到的信任。
- 因为一名玩家知道你在场，否认游戏中有人中毒通常会比论证中毒不在某个地方更难一些。你会给善良阵营带来巨大伤害，所以如果没有特别明显的证据的话他们不会放弃你潜伏在游戏中的想法。误导他们谁是中毒者会显得更合理。
- 如果你在一局剧本上有寡妇的游戏中是一名邪恶玩家，一个勇敢但有潜在回报的玩法是宣称你被告知了寡妇在场。这能让小镇信任你而在没有真正寡妇在场的情况下怀疑他们自己的能力。
- 在小型（五到八人）游戏中，对首夜能力角色投毒，比如调查员或钟表匠。这能确保你的中毒能带来切实的效果，并避免因你中毒的玩家在他们获得错误信息之前被恶魔杀死或被处决。
- 在大型（十二人以上）游戏中，避免对首夜能力角色投毒，转而对每晚都能获得信息的角色投毒，比如侍女或是神谕者，或是强力的非信息角色比如贞洁者或旅店老板。在大型游戏中，中毒玩家因为意外地处决或因为恶魔在有机会和你沟通之前就杀掉他们而导致死亡的几率要低很多，而你则有更多的机会调和局势来让他们的中毒发挥全部效果。
