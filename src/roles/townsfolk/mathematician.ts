- 你的信息是很有用的，但前提是你得弄清楚其他人都做了什么。和你的队友们沟通，得到他们的信息，尽可能找到谁的信息不正确或能力没有正常生效——这将对善良阵营有着巨大的帮助。一些类似于钟表匠、卖花女孩、女裁缝和神谕者等等的角色会需要你的帮助来确保他们的信息是正确的。
